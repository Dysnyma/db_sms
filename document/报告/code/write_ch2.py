body('以下需求不局限于单一角色，而是横跨整个系统的全局性需求，需要在系统设计层面统一考虑。')

body('（1）基于角色的访问控制（RBAC）。三种用户角色登录后应看到不同的功能菜单：学生只能看到学生端功能，教师只能看到教师端功能，教务能看到全部管理功能。这通过统一的认证模块来实现——根据用户输入的标识（学号/工号/admin）在对应的表中匹配身份，将角色信息写入会话，各页面根据角色动态渲染功能列表。')

