doc = Document()
style = doc.styles['Normal']
style.font.name = 'Times New Roman'; style.font.size = Pt(10.5)
style.element.rPr.rFonts.set(qn('w:eastAsia'), '宋体')
style.paragraph_format.line_spacing = 1.25

def cn(run, cn_f='宋体', en_f='Times New Roman'):
    rPr = run._r.get_or_add_rPr()
    rf = OxmlElement('w:rFonts')
    rf.set(qn('w:eastAsia'),cn_f); rf.set(qn('w:ascii'),en_f); rf.set(qn('w:hAnsi'),en_f)
    rPr.insert(0,rf)

def h1(text):
    p = doc.add_paragraph(); p.style = doc.styles['Heading 1']
    r = p.add_run(text); r.font.size = Pt(16); r.bold = True; r.font.name = 'Arial'; cn(r,'黑体','Arial')

def h2(text):
    p = doc.add_paragraph(); p.style = doc.styles['Heading 2']
    r = p.add_run(text); r.font.size = Pt(14); r.bold = True; r.font.name = 'Arial'; cn(r,'黑体','Arial')

def h3(text):
    p = doc.add_paragraph(); p.style = doc.styles['Heading 3']
    r = p.add_run(text); r.font.size = Pt(12); r.bold = True; r.font.name = 'Arial'; cn(r,'黑体','Arial')

def body(text):
    p = doc.add_paragraph(); p.paragraph_format.first_line_indent = Pt(21)
    r = p.add_run(text); r.font.size = Pt(10.5); r.font.name = 'Times New Roman'; cn(r)

def quote(text):
    p = doc.add_paragraph()
    p.paragraph_format.first_line_indent = Pt(0); p.paragraph_format.left_indent = Pt(21)
    r = p.add_run(text); r.font.size = Pt(10); r.font.name = 'Times New Roman'; r.italic = True; cn(r)
    r.font.color.rgb = RGBColor(0x66,0x66,0x66)

def bold_para(text):
    p = doc.add_paragraph(); p.paragraph_format.first_line_indent = Pt(0)
    r = p.add_run(text); r.font.size = Pt(10.5); r.bold = True; r.font.name = 'Times New Roman'; cn(r)

