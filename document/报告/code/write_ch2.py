bold_para('一、学生端业务需求')

body('学生是本系统的核心使用者之一。根据任务书第 119~127 条的要求，学生端需要实现浏览可选课程、选课、退选、成绩查询和学期均分查询共五项功能。')

body('（1）浏览可选课程。学生在每个学期初需要查看当前有哪些课程可以选。系统应向学生展示处于选课期内的排课列表，每一项排课应显示：课程名称、学分、授课教师姓名、当前已选人数与人数上限、剩余名额、选课起止时间。根据任务书第 126 条"同一门课程只能选择某个教师讲授的该课程"，同一门课程如果有多个教师在讲授，学生只能选择其中一位教师的排课。因此，可选课程列表应能清晰展示同一门课程下有哪些教师可供选择。')

body('（2）选课操作。根据任务书第 119~126 条，选课涉及以下业务规则，这些规则需要在选课功能中逐一校验：规则一，学生必须在选课有效期内操作（NOW() 需在 enroll_start_time 和 enroll_end_time 之间）；规则二，选课不分班级，所有学生在相同条件下公平竞争名额；规则三，同一门课程只能选一个教师——如果学生已经选了"张三的高等数学"，就不能再选"李四的高等数学"；规则四，某教师讲授的某课程选满后（current_students >= max_students），任何学生都不能再选；规则五，选课后，在选课到期时间之前可以退选。这五条规则意味着选课操作需要进行四重校验：学生身份验证 → 选课时效验证 → 重复选课检查（同一排课 + 同课不同教师） → 名额检查。任何一步不满足都应当给出明确的中文错误提示，而不是让数据库抛出晦涩的系统错误码。')

body('（3）退选操作。根据任务书第 127 条，学生在选课到期时间之前可以退选已选课程并重新选课。退选的核心约束是时效性——必须在选课截止时间（enroll_end_time）之前。退选后，该排课的名额应当自动释放（current_students 减 1），供其他学生选修。根据任务书第 2.3 节"生产业务数据不允许物理删除只能逻辑删除，以实现数据痕迹保留"的要求，退选不应使用 DELETE 语句物理删除选课记录，而应通过将 enrollment 表的 is_deleted 字段从 0 改为 1 来实现逻辑删除。这样，退选行为本身作为一条数据痕迹被保留下来，后续可以追溯"这个学生曾经选过这门课后来又退了"。')

body('（4）成绩查询。根据任务书第 136 条，学生可以查询自己选修的各门课程的成绩以及获得的学籍分。学生登录后进入成绩页面，系统应展示该学生所有已完成成绩录入的课程的成绩明细（课程名、授课教师、学期、成绩分数），并在显眼位置显示学籍分（加权平均分）和绩点分（GPA）的汇总数据。')

body('（5）学期均分查询。根据任务书第 138 条，学生可以按学期统计自己的平均成绩。学生输入学期信息（如"2025-2026-1"），系统返回该学期所修课程的数量以及各科成绩的平均分。')

