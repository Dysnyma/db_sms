h2('2.3 数据字典')

body('根据需求分析的结果，整理出系统涉及的 8 个核心数据项及其定义。以下按照"一个数据项一条描述"的格式编排，内容包括数据项的中文名称、英文名称、唯一标识方式、关键属性和业务状态说明。')

body('1. 学生（Student）。学号（no）为唯一标识。核心属性：姓名（name）、所属班级（class_id，外键关联 class 表）、学籍分（weighted_score，DECIMAL(5,2)，默认 0.00）、绩点分（gpa，DECIMAL(5,2)，默认 0.00）。业务状态：在读（status=1）或离校（status=0）。学籍分和绩点分由系统在成绩变化时通过触发器自动计算维护，不允许用户手动修改。一位学生属于一个班级（N:1），一位学生可以有多条选课记录（1:N）。')

body('2. 班级（Class）。班级名（name）为业务标识，但以自增 ID 为主键。核心属性：年级（grade，VARCHAR(10)）、专业（major，VARCHAR(100)）。业务状态：在读（status=1）或毕业（status=0）。一个班级包含多名学生，形成 1:N 关系。')

body('3. 课程（Course）。课程名（name）为唯一标识（UNIQUE 约束）。核心属性：学分（credit，DECIMAL(3,1)）。业务状态：开课（status=1）或停开（status=0）。一门课程可以有多次排课（1:N），每次排课由不同的教师在特定学期讲授。')

body('4. 教师（Teacher）。工号（no）为唯一标识（UNIQUE 约束）。核心属性：姓名（name）、职称（title，VARCHAR(50)，可空）、联系电话（phone，VARCHAR(20)，可空）。业务状态：在职（status=1）或离职（status=0）。一位教师可以讲授多门课程（M:N，通过 teacher_course 中间表），可以有多次排课（1:N）。')

body('5. 选课安排（Course Offering）。自增 ID 为主键。核心属性：关联课程（course_id，外键）和授课教师（teacher_id，外键）、学期（semester，VARCHAR(20)，如"2025-2026-1"）、选课人数上限（max_students，INT）、当前已选人数（current_students，INT，默认 0，由插入/更新触发器自动维护）、三个时间节点——选课开始时间（enroll_start_time，DATETIME）、选课截止时间（enroll_end_time，DATETIME）、成绩录入截止时间（grade_deadline，DATETIME）。业务状态：有效（status=1）或取消（status=0）。选课安排是整个选课业务的核心实体——它连接了课程和教师，并为学生的选课提供了具体的"选项"。')

body('6. 选课记录（Enrollment）。自增 ID 为主键，业务唯一键为 (offering_id, student_id) 组合，通过 UNIQUE 索引保证同一学生对同一排课不会产生重复选课。核心属性：关联选课安排（offering_id，外键）、关联学生（student_id，外键）、成绩（score，DECIMAL(5,2)，允许 NULL 表示尚未录入）。逻辑删除标记：is_deleted=0 表示选课中，is_deleted=1 表示已退选。选课记录是实现学生与选课安排之间 M:N 关系的具体载体——一条选课记录代表一名学生选修了一次排课。')

body('7. 讲授关系（Teacher Course）。自增 ID 为主键，业务唯一键为 (teacher_id, course_id) 组合，通过 UNIQUE 索引保证同一教师对同一课程不会产生重复的资格记录。核心属性：关联教师（teacher_id，外键）和课程（course_id，外键）。该表是教师与课程之间 M:N 关系的实现——记录每位教师有资格讲授哪些课程。排课时，教务只能从 teacher_course 表中有记录的（教师, 课程）组合中选择。')

body('8. 绩点规则（Grade Point Rule）。自增 ID 为主键。核心属性：成绩下限（min_score，DECIMAL(5,2)）、成绩上限（max_score，DECIMAL(5,2)）、对应绩点（point，DECIMAL(3,1)）。该表来源于任务书表 2-1，初始包含 5 条规则：0~59.99→0、60~69.99→1、70~79.99→2、80~89.99→3、90~100→4。该表被成绩自动计算触发器引用——通过 BETWEEN 匹配将百分制成绩转换为绩点值，进而参与 GPA 的计算。该表数据量极小（仅 5 行）且内容相对固定，属于配置型实体。')

body('以上 8 个数据项构成了系统的核心数据模型。其中，学生、班级、课程、教师是基础实体（存储系统中"有什么"的基本信息）；选课安排和选课记录是核心业务实体（驱动选课、退选、成绩录入等主要业务流程）；讲授关系是 M:N 中间实体（解耦教师和课程之间的多对多关联）；绩点规则是配置型实体（为业务计算提供参数化支持）。各数据项之间的精确关系在第三章的概念结构设计（E-R 图）中进一步形式化表达。')

