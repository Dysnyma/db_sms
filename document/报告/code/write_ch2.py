h3('2.4.2 AI输出的实体与业务规则初稿（截图或文字引用）')

body('AI 返回了以下结构化的提取结果：')

body('AI 识别出了 6 个核心实体：学生（Student）、班级（Class）、课程（Course）、教师（Teacher）、选课安排（Course Offering）、选课记录（Enrollment）。对每个实体，AI 给出了初步的属性列表。例如对学生实体，列出了学号（唯一标识）、姓名、所属班级（外键）、学籍分；对选课安排实体，列出了关联课程、授课教师、学期、人数上限、选课起止时间。关系方面，AI 识别出了"学生-班级（N:1）""学生-课程（M:N，通过选课记录实现）""教师-课程（M:N，通过选课安排实现）"等几组关系。')

body('在整体结构上，AI 的输出给出了一个"大致正确"的框架——识别出了大部分关键实体和它们之间的基本关系，为后续的概念设计提供了一个可用的出发点。但是，AI 的输出中存在以下几处需要人工修正的问题。')

