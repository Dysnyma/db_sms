h3('2.4.3 人工修正说明')

body('针对 AI 输出中的四类问题，我进行了以下人工修正和补充。每项修正均说明了修改内容、修改理由以及修正所依据的任务书条款或数据库设计原则。')

body('修正一：增加了"讲授关系（Teacher Course）"实体。在 AI 的模型中，教师直接通过 course_offering 表与课程关联。但实际上，教师和课程之间存在两层关系：第一层是"资格层"——某位教师有资格讲授哪些课程（通过 teacher_course 表记录）；第二层是"安排层"——在某个具体的学期，教务安排某位有资格的教师实际讲授一次课（通过 course_offering 表记录）。如果没有第一层，就可能出现"教务将一位教师安排到他没有资格讲的课程上"这一逻辑漏洞。因此，人工增加了 teacher_course 中间表，用 UNIQUE(teacher_id, course_id) 保证同一教师对同一课程只有一条资格记录。这一修正的依据是任务书第 121 条"一门课程可以由多个教师讲授，一个教师可以讲授多门课程"——这句话描述的 M:N 关系本身就需要一个独立的中间表来实现，而不是通过 course_offering 这种带业务属性的表来承担。')

body('修正二：将退选的实现方式明确为逻辑删除。AI 将退选理解为"删除选课记录"，但根据任务书的设计原则（不允许物理删除），所有删除操作都应通过逻辑删除来实现。具体到退选场景：退选 → UPDATE enrollment SET is_deleted=1 → 触发器检测到 is_deleted 从 0 变为 1 → 自动将 course_offering.current_students 减 1。这一系列连锁动作的前提是"退选 = 逻辑删除"，如果退选用的是物理 DELETE，触发器就捕获不到这个动作（因为 AFTER UPDATE 触发器不会在 DELETE 时触发），也就无法自动释放名额。因此，将退选设计为逻辑删除不仅是数据痕迹保留的需要，也是触发器能够正确工作的必要条件。')

body('修正三：将选课安排的时间维度从一个扩展为三个独立字段。AI 将选课安排的时间笼统概括为"有效时间"，但根据任务书第 122~125 条原文：第 122 条说"选课安排有生效时间，生效后学生才能选课"，第 123 条说"选课安排有选课到期时间限制，达到时间限制后不能再选"，第 125 条说"选课安排有成绩录入到期时间限制，达到时间限制后不能再修改成绩"。三条描述对应三个不同的时间节点，它们控制的行为是相互独立的。因此，course_offering 表需要三个独立的 DATETIME 字段——enroll_start_time、enroll_end_time、grade_deadline。这一修正确保了需求分析的结果与任务书的原始描述保持一致。')

body('修正四：补充了绩点对照表（Grade Point Rule）实体。该实体来源于任务书 7.2.2 节表 2-1，定义了从百分制成绩到绩点的映射关系。虽然这个实体是在"运维需求变更"章节中提出的，但因为 GPA 的计算需要贯穿整个系统（从成绩录入触发器的重算，到学生成绩查询页面的展示），它将作为一个持久化的配置表存在，而不是临时性的需求。将绩点规则设计为一个独立的数据库表（而不是在代码中硬编码 IF-THEN 逻辑），有以下好处：如果将来绩点对照标准发生调整（例如教务决定修改某个区间的绩点值），只需 UPDATE grade_point_rule 表中的数据即可，不需要修改任何触发器或存储过程的代码——这体现了数据与逻辑分离的设计原则。')

body('总的来说，需求分析阶段的 AI 使用方式与任务书路径 B 的定位一致——AI 帮助我从长文本中快速提取和整理信息，输出的结果作为一个"初始草稿"使用。但 AI 的输出存在信息粒度不够细（时间属性被笼统化）、隐含实体识别不足（漏掉讲授资格）、以及容易忽略跨章节信息关联（漏掉绩点对照表）等问题。这些问题通过自己仔细对照任务书原文、思考业务逻辑、以及运用数据库设计的基本原则（实体完整性、逻辑删除、数据与逻辑分离）进行了逐项修正。最终的需求文档中的所有核心判断——哪些数据项需要独立成表、关系是 1:N 还是 M:N、删除用物理方式还是逻辑方式——均由自己做出，AI 没有替代我做任何一个设计决策。')

