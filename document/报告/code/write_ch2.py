body('问题二：AI 没有识别出"讲授资格"这个隐含的实体。AI 直接将教师和课程通过选课安排关联，但忽略了需求中的这条隐含规则——一位教师需要先"有资格"讲授某门课程，然后才能被教务安排到该课程的排课中。这说明 AI 在识别"隐含实体"方面的能力有限，它倾向于只从字面上提取信息，而不会主动思考字面背后可能存在的业务约束。')

