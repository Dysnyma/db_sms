body('按照任务书附录一的要求，使用 AI 辅助设计时需要在报告中包含：所使用的工具及版本、向 AI 输入的原始需求描述（提示词）、AI 输出的结果（截图或文字引用）、人工审核所做的修改和补充、以及修改的理由。以下逐项记录需求分析阶段 AI 的使用情况。')

body('需求分析是理解"系统要做什么"的过程。本次课程设计的选题为任务书题目二，任务书 7.2.1 节已给出了相当详细的基本需求描述（从第 115 条到第 141 条，涵盖 26 条具体需求和 23 项功能要求），包括业务规则、数据关系、计算公式和报表需求。因此，需求分析阶段的主要工作不是"从零开始挖掘需求"，而是"在任务书已有描述的基础上，结合自己对教务流程的理解，将分散的需求条目重新组织成一个结构化的需求文档，并识别出其中隐含的业务规则和实体关系"。AI 在此阶段的作用是帮助快速提取和组织信息，而理解和判断则由人工完成。')

h3('2.4.1 输入AI的原始需求描述（提示词/Prompt）')

