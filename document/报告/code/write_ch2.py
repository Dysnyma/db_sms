body('（2）基础数据管理。教务需要对班级（class）、课程（course）、教师（teacher）、学生（student）四类基础数据进行增删改查维护。重要约束：所有删除操作均采用逻辑删除（UPDATE is_deleted=1），不物理删除数据；根据任务书第 132 条"学生基本信息的获取要求使用视图，因为学生表上的学籍分在常规增删改查时不需要显示和查看，应该使用自己的外模式"，学生信息的展示应通过视图实现，将学籍分和绩点分字段对外屏蔽。')

